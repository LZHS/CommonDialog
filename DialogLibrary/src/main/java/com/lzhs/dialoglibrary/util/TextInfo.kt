package com.lzhs.dialoglibrary.util

object TextInfo {
    /**
     * 字号大小，值为-1时使用默认样式，单位：dp
     */
    var fontSize = -1

    /**
     * 对齐方式，值为-1时使用默认样式，取值可使用Gravity.CENTER等对齐方式
     */
    var gravity = -1

    /**
     * 文字颜色，值为1时使用默认样式，取值可以用Color.rgb(r,g,b)等方式获取
     */
    var fontColor = 1

    /**
     * 是否粗体
     */
    var bold = false
}